// ARRAY
let fruits = ["apple", "mango", "grapes"];
console.log(fruits);
fruits[1] = 'banana';
console.log(fruits);

// Array push pop
fruits.push("lichie");
console.log(fruits);

fruits.pop();
console.log(fruits.pop());
console.log(fruits);

// array shift unshift
console.log(fruits.unshift("kiwi"));

fruits.shift();
console.log(fruits);